      "def return_early(value) =(return \"negative\" if value<0;return \"zero\" if value==0;\"positive\");",
      "def multi_assign;first,*rest=1,2,3,4;first+rest.length;",
      "end;",
      "def splat_args(*args,**kwargs) =args.length+kwargs.length;",
      "def with_block(&block) =(block.call @current_value if block);",
      "def use_lambda =(fn=->(val){val*2};fn.call @current_value);",
