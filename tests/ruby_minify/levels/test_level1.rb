      "alias neg subtract_number;",
      "def multiply_by_offset =@current_value*Calculator::OFFSET;",
      "def reset_value =(@current_value=0;counter=0;counter+=1 while counter<3;$last_calculator=nil;counter);",
      "def get_separator =?x;",
      "def describe_value =@current_value>100?\"big\":\"small\";",
      "def check_positive =(\"non-positive\" unless @current_value>0);",
      "def process_items(items) =items.each{|item|puts item};",
      "def safe_divide(divisor) =begin;@current_value/divisor;rescue ZeroDivisionError;0;ensure;@history.push :divide;",
      "end;",
      "def classify_value =case @current_value;when (0..10);",
      "\"low\";when (11..100);",
      "\"medium\";else;\"high\";",
      "end;",
      "def value_with_label =\"\#{@label}: \#{@current_value}\";",
      "def to_s =\"Calculator(\#{@current_value})\";",
      "def parent_info =super;",
      "def with_yield =(yield @current_value if block_given?);",
      "def count_up(limit) =(result=[];idx=0;until idx>=limit;idx+=1;next if idx==2;break if idx>4;result.push idx;",
      "end;result);",
      "def retry_example =(attempts=0;begin;attempts+=1;raise \"fail\" if attempts<2;attempts;rescue;retry if attempts<2;0;end);",
