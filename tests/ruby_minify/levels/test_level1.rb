  include MinifyTestHelper
  include LevelTestHelper

  # L1: Syntax optimizations — boolean shorten, char shorten, constant fold,
  #     control flow simplify, endless method, paren optimizer
  def test_level_1
    result = minify_at_level(LEVEL_TEST_CODE, 1)
    expected = [
      "module MathUtils;MULTIPLIER=5;S=1000000000000000000;",
      "def self.double_value(number) =number*MULTIPLIER*S;",
      "def self.clamp_value(value,min_val,max_val) =value<min_val ? min_val : value>max_val ? max_val : value;",
      "end;",
      "class Base;",
      "def parent_info =\"base\";",
      "end;",
      "class Calculator<Base;",
      "OFFSET=256;",
      "LABELS=%w[low medium high].freeze;",
      "SYMBOLS=%i[add subtract multiply].freeze;",
      "attr_reader :current_value;",
      "attr_accessor :label;",
      "def initialize(initial_value,label:\"default\") =(@current_value=initial_value;@label=label;@history=[];@@instance_count||=0;@@instance_count+=1;$last_calculator=self);",
